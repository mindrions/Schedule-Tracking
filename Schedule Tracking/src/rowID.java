
public class rowID {

	String color;
	String reason;

	public rowID(String color, String reason) {
		this.color = color;
		this.reason = reason;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

}
